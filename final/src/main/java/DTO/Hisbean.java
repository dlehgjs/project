package DTO;

public class Hisbean {
	
	private int his_num;
	private String order_code;
	private int pro_num;
	private int his_amount;
	private int his_price;
	
	public int getHis_num() {
		return his_num;
	}
	public void setHis_num(int his_num) {
		this.his_num = his_num;
	}
	public String getOrder_code() {
		return order_code;
	}
	public void setOrder_code(String order_code) {
		this.order_code = order_code;
	}
	public int getPro_num() {
		return pro_num;
	}
	public void setPro_num(int pro_num) {
		this.pro_num = pro_num;
	}
	public int getHis_amount() {
		return his_amount;
	}
	public void setHis_amount(int his_amount) {
		this.his_amount = his_amount;
	}
	public int getHis_price() {
		return his_price;
	}
	public void setHis_price(int his_price) {
		this.his_price = his_price;
	}
	

}
